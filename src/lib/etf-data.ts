export function searchETFProducts(query: string, category?: string): ETFProduct[] {
  let results = ETF_DATABASE;

  if (category) {
    results = results.filter(
      (etf) =>
        etf.category === category ||
        etf.subCategory === category ||
        etf.category.includes(category) ||
        etf.subCategory.includes(category)
    );
  }

  if (!query || query.trim() === "") {
    return results.slice(0, 20);
  }

  const q = query.toLowerCase();
  const keywords = q.split(/\s+/);

  results = results.filter((etf) => {
    const searchText = `${etf.name} ${etf.ticker} ${etf.index} ${etf.description} ${etf.category} ${etf.subCategory}`.toLowerCase();
    return keywords.every((kw) => searchText.includes(kw));
  });

  return results.slice(0, 20);
}

export function getETFDetail(ticker: string): ETFProduct | null {
  const etf = ETF_DATABASE.find((e) => e.ticker === ticker);
  if (!etf) {
    // 이름으로도 검색
    const byName = ETF_DATABASE.find((e) =>
      e.name.toLowerCase().includes(ticker.toLowerCase())
    );
    if (byName) {
      return { ...byName, priceHistory: generatePriceHistory(byName.nav, byName.return1Y) };
    }
    return null;
  }
  return { ...etf, priceHistory: generatePriceHistory(etf.nav, etf.return1Y) };
}

export function compareETFs(tickers: string[]): ETFProduct[] {
  return tickers
    .map((t) => {
      const exact = ETF_DATABASE.find((etf) => etf.ticker === t);
      if (exact) return exact;
      return ETF_DATABASE.find((etf) =>
        etf.name.toLowerCase().includes(t.toLowerCase())
      );
    })
    .filter(Boolean) as ETFProduct[];
}

